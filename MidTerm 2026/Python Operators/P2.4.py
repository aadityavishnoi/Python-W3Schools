# Initial value
x = 10

print("Initial value:", x)

# Using assignment operators
x += 5   # x = x + 5
print("After += :", x)

x -= 3   # x = x - 3
print("After -= :", x)

x *= 2   # x = x * 2
print("After *= :", x)

x /= 4   # x = x / 4
print("After /= :", x)

x %= 3   # x = x % 3
print("After %= :", x)